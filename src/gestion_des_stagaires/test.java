    private void bRechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRechActionPerformed
       try { 
    dt.setRowCount(0); //pour vider tableau 
    Statement st = con.createStatement();
        ResultSet rs = st.executeQuery( "SELECT * FROM stagaire WHERE "+combo()
                +"  like '%"+txtrech.getText()+"%'" );
    
     while( rs.next()) {
                
                id=rs.getInt("id");
                mat=rs.getString("mat");
                nom=rs.getString("nom");
                prenom=rs.getString("prenom");
                service=rs.getString("service");
                dure=rs.getString("duree_de_stage");
                sexe=rs.getString("sexe");
                Object[] t={id,mat,nom,prenom,service,dure,sexe};
                dt.addRow(t);
                
                
            }
     
        if (dt.getRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Aucun Stagiaire");
            Afficher(); // afficehr tout les stgs
        }else{
            i = 0;
            Deplacer(i);
        }
    
}catch (Exception m){JOptionPane.showMessageDialog(null, ""+m.getMessage());
System.err.println(m);}
    }//GEN-LAST:event_bRechActionPerformed

