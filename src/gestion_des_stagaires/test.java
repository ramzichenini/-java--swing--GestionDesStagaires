private void Afficher() {
    
    
        try {
            txtid.setEnabled(false);
            
            dt.setRowCount(0); //vider la la liste des stagaires
            
            rs= st.executeQuery("select * from stagaire");
            while( rs.next()) {
                
                id=rs.getInt("id");
                mat=rs.getString("mat");
                nom=rs.getString("nom");
                prenom=rs.getString("prenom");
                service=rs.getString("service");
                dure=rs.getString("duree_de_stage");
                sexe=rs.getString("sexe");
                Object[] t={id,mat,nom,prenom,service,dure,sexe};
                dt.addRow(t);
                
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(test.class.getName()).log(Level.SEVERE, null, ex);
        }
        

}

    private void Deplacer(int i) {
     try{
        if (dt.getRowCount() == 0){ //si il y aucun enregistrement tableau est vide
            btnprecedent.setEnabled(false);
            btnsuivant.setEnabled(false);
            actualiser();
        }else{
            if(i == 0){ //si i est localiser sur le premier enregistrement  
                btnprecedent.setEnabled(false);
                btnsuivant.setEnabled(true);
                }else if (i == dt.getRowCount()-1){ //  si i est localisÃ© sur  dernier enregistrement
                btnprecedent.setEnabled(true);
            btnsuivant.setEnabled(false);
            }else{
                btnprecedent.setEnabled(true);
                btnsuivant.setEnabled(true);
            }
            if(dt.getRowCount() == 1){//si il y a un seul enregistrement, nous avons un seul ligne
                btnprecedent.setEnabled(false);
                btnsuivant.setEnabled(false);
            }
        }
        
        txtid.setText(dt.getValueAt(i, 0).toString()); 
        txtmat.setText(dt.getValueAt(i, 1).toString()); // i represente numero d ligne ,et 0 1 .. colonne
        txtnom.setText(dt.getValueAt(i, 2).toString()); // lorseque i=1 getValueAt nous retourne le premier ligne si i=2 le ligne ......
       txtprenom.setText(dt.getValueAt(i, 3).toString());
        combservice.setSelectedItem(dt.getValueAt(i, 4).toString());
        txtdure.setText(dt.getValueAt(i, 5).toString());
        String s = dt.getValueAt(i, 6).toString();
        if(s.equals("Masculain")){j1.setSelected(true);}else if(s.equals("FÃ©minin")){j2.setSelected(true);}
        else {j3.setSelected(true);}
    }catch(Exception e){
         JOptionPane.showMessageDialog(null, "Erreur deplacement stagiaire\n"+e.getMessage());
    }
    }


